import pandas as pd
import ast

# HUMAN

def load_human(path: str = "people_with_prob.csv") -> pd.DataFrame:
    """
    Load human cloze data, keep only Russian answers,
    and recompute probabilities within each context.
    """
    df = pd.read_csv(path)

    df = df[df["is_russian"] == True].copy()  # noqa: E712

    # probability_y = proportion of respondents
    # who produced this answer within the context
    df["probability_y"] = df.groupby("word.id")["answer"].transform(
        lambda s: s.map(s.value_counts(normalize=True))
    )

    return df



def _normalize_model_df(df: pd.DataFrame) -> pd.DataFrame:
    """
    Brings model outputs to a unified interface:
      - prediction_cleaned
      - probability_converted
      - feature_accuracy
      - pred_stripped
    """

    rename_map = {}

    if "word" in df.columns and "prediction_cleaned" not in df.columns:
        rename_map["word"] = "prediction_cleaned"

    if "probability" in df.columns and "probability_converted" not in df.columns:
        rename_map["probability"] = "probability_converted"

    if (
        "feature_intersection" in df.columns
        and "feature_accuracy" not in df.columns
    ):
        rename_map["feature_intersection"] = "feature_accuracy"

    if rename_map:
        df = df.rename(columns=rename_map)

    # force float
    df["probability_converted"] = pd.to_numeric(
        df["probability_converted"],
        errors="coerce"
    )

    # stripped surface form
    df["pred_stripped"] = (
        df["prediction_cleaned"]
        .astype(str)
        .str.strip()
    )

    return df
# GPT4OMINI

def load_gpt(path: str = "gpt4omini_morph_2.csv") -> pd.DataFrame:
    """
    Load GPT-4o-mini predictions.
    Raw probabilities are preserved unchanged.
    """

    df = pd.read_csv(path)

    df = df[df["is_russian"] == True].copy()  # noqa: E712

    df = _normalize_model_df(df)

    # keep original probabilities explicitly
    df["probability_raw"] = df["probability_converted"]

    return df

# LLAMA

def _parse_prob_column(col: pd.Series) -> pd.Series:
    """
    Safely parses probability columns that may contain:
      - floats
      - strings
      - np.float16(...)
      - lists/tuples
    """

    def _parse(x):

        if pd.isna(x):
            return x

        if isinstance(x, (int, float)):
            return float(x)

        try:
            parsed = ast.literal_eval(str(x))

            # if list/tuple -> take first element
            if isinstance(parsed, (list, tuple)) and len(parsed) > 0:
                parsed = parsed[0]

            return float(parsed)

        except (ValueError, SyntaxError, TypeError):

            try:
                return pd.to_numeric(x, errors="coerce")

            except Exception:
                return pd.NA

    return col.apply(_parse)


def load_llama(path: str = "llama_with_all.csv") -> pd.DataFrame:
    """
    Load Llama predictions.

    Important methodological note:
    Llama probabilities were generated over a restricted space
    of Cyrillic continuations only.

    Therefore:
      - probability_raw stores original beam-search joint probabilities
      - probability_converted stores context-normalized probabilities

    The normalization is applied post hoc within each target_word_id
    to improve comparability with GPT-4o-mini rankings.
    """

    df = pd.read_csv(path)

    df = df[df["is_russian"] == True].copy()  # noqa: E712

    # remove trailing <mask>
    if "sentence_text" in df.columns:

        df["sentence_text"] = (
            df["sentence_text"]
            .str.replace(r"\s*<mask>\s*$", "", regex=True)
            .str.strip()
        )

    # parse probability columns safely
    for col in ["probability", "token_probabilities"]:

        if col in df.columns:
            df[col] = _parse_prob_column(df[col])

    # unify schema
    df = _normalize_model_df(df)

    # preserve original values
    df["probability_raw"] = df["probability_converted"]

    

    denom = (
        df.groupby("target_word_id")["probability_converted"]
        .transform("sum")
    )

    df["probability_converted"] = (
        df["probability_converted"] / denom
    )

    # avoid division artifacts
    df.loc[
        denom.isna() | (denom == 0),
        "probability_converted"
    ] = pd.NA

    return df



def dedup_by_lemma(model_df: pd.DataFrame) -> pd.Series:
    """
    For each context:
    keep only one prediction per lemma
    (highest probability wins).
    """

    sorted_df = model_df.sort_values(
        "probability_converted",
        ascending=False
    )

    return (
        sorted_df
        .drop_duplicates(
            subset=["target_word_id", "lemma_word"],
            keep="first"
        )
        .groupby("target_word_id", sort=False)
        .apply(
            lambda df: df["lemma_word"].tolist(),
            include_groups=False
        )
        .rename("model_lemmas")
    )


def dedup_by_surface(model_df: pd.DataFrame) -> pd.Series:
    """
    For each context:
    keep only one prediction per surface form.
    """

    sorted_df = model_df.sort_values(
        "probability_converted",
        ascending=False
    )

    return (
        sorted_df
        .drop_duplicates(
            subset=["target_word_id", "pred_stripped"],
            keep="first"
        )
        .groupby("target_word_id", sort=False)
        .apply(
            lambda df: df["pred_stripped"].tolist(),
            include_groups=False
        )
        .rename("model_surface")
    )



def get_all_models() -> dict:
    """
    Returns:
        {
            model_name: dataframe
        }
    """

    return {
        "GPT-4o-mini": load_gpt(),
        "Llama": load_llama(),
    }
